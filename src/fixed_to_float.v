/**
 * Fixed-to-Float Converter
 * Converts a signed fixed-point number to IEEE 754 Binary32 (Float32).
 * Handles shared scaling in the floating-point domain for maximum dynamic range.
 */
module fixed_to_float #(
    parameter WIDTH = 40,
    parameter FRAC_BITS = 16
)(
    input  wire [WIDTH-1:0] data_in,
    input  wire signed [9:0] shared_exp,
    input  wire             nan_sticky,
    input  wire             inf_pos_sticky,
    input  wire             inf_neg_sticky,
    output wire [31:0]      data_out
);
    wire sign = data_in[WIDTH-1];
    wire [WIDTH-1:0] abs_data = sign ? -data_in : data_in;

    reg [5:0] p;
    always @(*) begin
        casez (abs_data)
            40'b1???????????????????????????????????????: p = 6'd39;
            40'b01??????????????????????????????????????: p = 6'd38;
            40'b001?????????????????????????????????????: p = 6'd37;
            40'b0001????????????????????????????????????: p = 6'd36;
            40'b00001???????????????????????????????????: p = 6'd35;
            40'b000001??????????????????????????????????: p = 6'd34;
            40'b0000001?????????????????????????????????: p = 6'd33;
            40'b00000001????????????????????????????????: p = 6'd32;
            40'b000000001???????????????????????????????: p = 6'd31;
            40'b0000000001??????????????????????????????: p = 6'd30;
            40'b00000000001?????????????????????????????: p = 6'd29;
            40'b000000000001????????????????????????????: p = 6'd28;
            40'b0000000000001???????????????????????????: p = 6'd27;
            40'b00000000000001??????????????????????????: p = 6'd26;
            40'b000000000000001?????????????????????????: p = 6'd25;
            40'b0000000000000001????????????????????????: p = 6'd24;
            40'b00000000000000001???????????????????????: p = 6'd23;
            40'b000000000000000001??????????????????????: p = 6'd22;
            40'b0000000000000000001?????????????????????: p = 6'd21;
            40'b00000000000000000001????????????????????: p = 6'd20;
            40'b000000000000000000001???????????????????: p = 6'd19;
            40'b0000000000000000000001??????????????????: p = 6'd18;
            40'b00000000000000000000001?????????????????: p = 6'd17;
            40'b000000000000000000000001????????????????: p = 6'd16;
            40'b0000000000000000000000001???????????????: p = 6'd15;
            40'b00000000000000000000000001??????????????: p = 6'd14;
            40'b000000000000000000000000001?????????????: p = 6'd13;
            40'b0000000000000000000000000001????????????: p = 6'd12;
            40'b00000000000000000000000000001???????????: p = 6'd11;
            40'b000000000000000000000000000001??????????: p = 6'd10;
            40'b0000000000000000000000000000001?????????: p = 6'd9;
            40'b00000000000000000000000000000001????????: p = 6'd8;
            40'b000000000000000000000000000000001???????: p = 6'd7;
            40'b0000000000000000000000000000000001??????: p = 6'd6;
            40'b00000000000000000000000000000000001?????: p = 6'd5;
            40'b000000000000000000000000000000000001????: p = 6'd4;
            40'b0000000000000000000000000000000000001???: p = 6'd3;
            40'b00000000000000000000000000000000000001??: p = 6'd2;
            40'b000000000000000000000000000000000000001?: p = 6'd1;
            40'b0000000000000000000000000000000000000001: p = 6'd0;
            default: p = 6'd0;
        endcase
    end

    // Raw exponent: E = p - FRAC_BITS + 127 + shared_exp
    wire signed [11:0] e_full = $signed({6'd0, p}) - $signed({6'd0, FRAC_BITS[5:0]}) + 12'sd127 + $signed({{2{shared_exp[9]}}, shared_exp});

    // Mantissa and Rounding (RNE)
    // Align leading 1 to bit 127 of 128-bit word.
    // abs_data[p] is the leading 1. It is at bit p+88 of {abs_data, 88'd0}.
    // To move it to bit 127, shift left by 127 - (p + 88) = 39 - p.
    wire [127:0] shifted = {abs_data, 88'd0} << (6'd39 - p);
    wire [22:0] m_base = shifted[126:104];
    wire r = shifted[103];
    wire s = |shifted[102:0];
    wire m_inc = r && (s || m_base[0]);
    wire [23:0] m_rounded_full = {1'b0, m_base} + {23'd0, m_inc};
    wire m_overflow = m_rounded_full[23];
    wire [22:0] m_final = m_rounded_full[22:0];

    reg [7:0]  exponent;
    reg [22:0] mantissa;

    always @(*) begin
        if (data_in == 0) begin
            exponent = 8'd0;
            mantissa = 23'd0;
        end else if (e_full >= 255 || (e_full == 254 && m_overflow)) begin
            // Overflow to Infinity
            exponent = 8'hFF;
            mantissa = 23'd0;
        end else if (e_full <= 0) begin
            // Simplified: flush to zero (Float32 subnormals not implemented)
            exponent = 8'd0;
            mantissa = 23'd0;
        end else begin
            exponent = e_full[7:0] + {7'd0, m_overflow};
            mantissa = m_final;
        end
    end

    // Exception sticky logic
    assign data_out = (nan_sticky || (inf_pos_sticky && inf_neg_sticky)) ? 32'h7FC00000 :
                      inf_pos_sticky ? 32'h7F800000 :
                      inf_neg_sticky ? 32'hFF800000 :
                      {sign, exponent, mantissa};

endmodule
